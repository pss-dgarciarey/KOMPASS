function getCountryProfile(iso3) {
  const normalized = String(iso3 || '').trim().toUpperCase();
  const alpha2 = countries.alpha3ToAlpha2(normalized) || '';
  const name = alpha2 ? countries.getName(alpha2, 'en') : normalized;
  const benchmarkRaw = COUNTRY_BENCHMARKS[normalized];
  const benchmark = benchmarkRaw
    ? {
        provider: benchmarkRaw.provider || 'yahoo',
        ...benchmarkRaw,
        marketType: benchmarkRaw.proxy ? 'proxy' : 'tracked',
        statusLabel: benchmarkRaw.proxy ? 'ETF proxy' : 'Tracked index',
        venue: COUNTRY_MARKET_VENUES[normalized] || 'National exchange benchmark',
        detail: benchmarkRaw.proxy
          ? `${benchmarkRaw.label} is used as a free country-market proxy for ${name || normalized}.`
          : `${benchmarkRaw.label} is the tracked national benchmark for ${name || normalized}.`
      }
    : {
        provider: null,
        symbol: null,
        label: 'No tracked national benchmark',
        currency: '',
        proxy: false,
        marketType: 'untracked',
        statusLabel: 'No benchmark',
        venue: 'No tracked national exchange benchmark configured',
        detail: `No broad free equity benchmark is configured for ${name || normalized} right now.`
      };

  return {
    iso3: normalized,
    alpha2,
    name: name || normalized,
    benchmark,
    demonyms: COUNTRY_DEMONYMS[normalized] || [String(name || normalized).toLowerCase()]
  };
}

